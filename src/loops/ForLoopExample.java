package loops;

public class ForLoopExample {

	int num;

	// print the next 10 numbers

	void printConsecutive10Numbers() {

		// syntax for for loop //the counter variable needs to initialized inside the For Loop

		for (int i = 0; // i is the counter for loop or variable that runs the loop multipletimes
				i < 10; i++) {
			System.out.println(num);
			num++;
			
		}

	}

}
